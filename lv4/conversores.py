# conversor int

exemplo = int("1")
conta = 8 + exemplo
print(conta)
print(type(conta))
print('_____________________________________\n')

# conversor float

exemploF = float(1)
contaF = 8 + exemploF
print(contaF)
print(type(contaF))
print('_____________________________________\n')

# conversor str

exemploS = str(1)
contaS = '8' + exemploS
print(contaS)
print(type(contaS))